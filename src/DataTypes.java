public class DataTypes {
    public static void main(String[] args) {
        // Примитивные типы данных
        byte num1 = 127; // 8 бит от -128 до 127
        short num2 = 5000; // 16 бит от -32768 до 32767
        int num3 = 1000000; // 32 бит  от -2147483648 до 2147483647
        long num4 = 2000000000; // 64 бит от -9 223 372 036 854 775 808 до  9 223 372 036 854 775 807

        float num5 = 1.2f; // 32 после точки 6-7 знаков
        double num6 = 1.11; // 64 после точки 15 знаков

        char sym = '\u2122'; // 16 бит

        boolean isTrue = false; // 1 бит
    }
}